// Get songs for a specific mood (returns 6 random songs)
export const getSongsForMood = (mood) => {
    const moodLower = mood.toLowerCase().replace(/\s+/g, '');
    
    // Find matching mood category
    let songs = MOOD_SONGS[moodLower];
    
    // If no exact match, try to find partial match
    if (!songs) {
        const moodKeys = Object.keys(MOOD_SONGS);
        const matchedKey = moodKeys.find(key => 
            moodLower.includes(key) || key.includes(moodLower)
        );
        songs = matchedKey ? MOOD_SONGS[matchedKey] : MOOD_SONGS.netral;
    }
    
    // Shuffle and return 6 songs
    const shuffled = [...songs].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, 6);
};

